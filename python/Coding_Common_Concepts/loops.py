'''Python counting script
V1.0'''

# Here we use while loop

count = 0
while count < 5:
    print(count)
    count += 1 # This is the same as count plus one.
print("Finished!")

'''Python items list
V1.0'''

# Here we use for loop

myShoppingList = ["Apples", "Oranges", "Bread", "Milk"]
for item in myShoppingList:
    print(item) # Here we see a list of items.

